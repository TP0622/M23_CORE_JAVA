package com.capg.looping;
//Printing Right angle triangle Hash(#) pattern
public class RtriangleProgram {

	public static void main(String[] args) 
	{
		int i, k;
		for (i=0;i<=5;i++)
		{
			for (k=0;k<=i;k++)
			{
				System.out.print('#');//Prints # character each itteration.
			}
		    System.out.println();//Prints new line
		}
		

	}

}
