package com.capg.looping;
//Assignment Enhanced for loop.
public class EnhancedForLoop {

	public static void main(String[] args) 
	{
		//Creating an array
		int[] numbers = {22,44,66,88};
		
		//Enhanced for loop.
		for(int number:numbers) {
			System.out.println(number);
		}

	}

}
