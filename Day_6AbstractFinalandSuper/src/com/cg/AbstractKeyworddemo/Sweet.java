package com.cg.AbstractKeyworddemo;
//abstract class
class Sweet extends Rasmalai
{
	//constructor
	public Sweet()
	{
		System.out.println(" I like rasmalai ");
	}
	//non abstract method
	public String taste()
	{
		return "Sweet";
	}
	//implementation for abstract method
	
	void print()
	{
		System.out.println("Sweet is Rasmalai");
	}

}

